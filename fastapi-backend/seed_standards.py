def generate_insert_sql() -> str:
    """Generate SQL INSERT statements for all seed standards."""
    now = datetime.now(timezone.utc).isoformat()
    values = []
    for cat, ct, desc, result, clause, order in SEED_STANDARDS:
        uid = str(uuid.uuid4())
        desc_escaped = desc.replace("'", "''")
        result_escaped = result.replace("'", "''")
        clause_escaped = clause.replace("'", "''")
        values.append(
            f"('{uid}', '{cat}', '{ct}', '{desc_escaped}', "
            f"'{result_escaped}', '{clause_escaped}', {order}, "
            f"'{now}', '{now}')"
        )
    return (
        "INSERT INTO evaluation_standards "
        "(id, category, component_type, description, evaluation_result, "
        "evaluation_clause, sort_order, created_at, updated_at) VALUES\n"
        + ",\n".join(values)
        + ";"
    )


if __name__ == "__main__":
    import sys, os
    sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
    import asyncio
    from sqlalchemy import text
    from app.db.database import async_session_factory

    async def seed():
        sql = generate_insert_sql()
        async with async_session_factory() as session:
            await session.execute(text("DELETE FROM evaluation_standards"))
            await session.execute(text(sql))
            await session.commit()
        print(f"Inserted {len(SEED_STANDARDS)} evaluation standards.")

    asyncio.run(seed())
